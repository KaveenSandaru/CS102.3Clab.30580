#include <stdio.h>
int main()
{
    printf("Upper case letters : \n");
    printf("A : %d\n",'A');
    printf("B : %d\n",'B');
    printf("C : %d\n",'C');
    printf("Lower case letters : \n");
    printf("a : %d\n",'a');
    printf("b : %d\n",'b');
    printf("c : %d\n",'c');
    printf("Integers : \n");
    printf("0 : %d\n",'1');
    printf("1 : %d\n",'2');
    printf("2 : %d\n",'3');
    printf("Symbols : \n");
    printf("$ : %d\n",'$');
    printf("* : %d\n",'*');
    printf("+ : %d\n",'+');
    printf("/ : %d\n",'/');
    printf("Blank character : %d\n",' ');
}
